public class Operacoes {

	public static void main(String[] args) {
//	Exemplo adicao
		double adicao = 5 + 2;
//	Exemplo subtracao	
		double subtracao = 5 - 2;
//	Exemplo multiplicacao	
		double multiplicacao = 5 * 2;
//	Exemplo divisao	
		double divisao = 10 / 2;
//	Exemplo modulo	
		double modulo = 10 % 2;
		
//		mostra resultados
		System.out.println("5 + 2 = "+adicao);
		System.out.println("5 - 2 = " +subtracao);
		System.out.println("5 * 2 = "+multiplicacao);
		System.out.println("10 / 2 = "+divisao);
		System.out.println("10 % 2 = "+modulo);
	}

}
